         <h1>Boruah Block High School :</h1>
         <b>BORUAH BLOCK HS
BORUAH BLOCK HS</b> was established in 1994 and it is managed by the Department of Education. It is located in Rural area. It is located in DHEMAJI block of DHEMAJI district of Assam. The school consists of Grades from 9 to 10. The school is Co-educational and it doesn't have an attached pre-primary section. The school is Not Applicable in nature and is not using school building as a shift-school. Assamese is the medium of instructions in this school. This school is approachable by all weather road. In this school academic session starts in April.
         The school has Government building. It has got 0 classrooms for instructional purposes. All the classrooms are in good condition. It has 2 other rooms for non-teaching activities. The school has a separate room for Head master/Teacher. The school has No Boundary Wall boundary wall. The school has have electric connection. The source of Drinking Water in the school is Hand Pumps and it is functional. The school has 1 boys toilet and it is functional. and 1 girls toilet and it is functional. The school has no playground. The school has a library and has 121 books in its library. The school does not need ramp for disabled children to access classrooms.The school has no computers for teaching and learning purposes The school is not having a computer aided learning lab. The school is Not Applicable providing mid-day meal.

